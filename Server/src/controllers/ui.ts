import * as express from "express";


//--------------------------------------------------------------------------------------------------
// Module Section
//--------------------------------------------------------------------------------------------------
module UIRoute
{
    //----------------------------------------------------------------------------------------------
    // Route Controllers Section
    //----------------------------------------------------------------------------------------------
    export class IndexController
    {

        //------------------------------------------------------------------------------------------
        // Public WebMethods Section
        //------------------------------------------------------------------------------------------
        public getIndex(req: express.Request, res: express.Response, next: express.NextFunction)
        {
            let state: {} = {
                "PageTitle" : "Digital Mechanic 1.0 beta!",
                "user"      : req.user
            };

            res.render("index", state);
        }
    }
}

