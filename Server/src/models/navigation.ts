//--------------------------------------------------------------------------
// Imports Section:
//--------------------------------------------------------------------------
import { Link } from "./link";


//--------------------------------------------------------------------------
// Model Class Navigation:
//--------------------------------------------------------------------------
export class Navigation
{
    //----------------------------------------------------------------------
    // Private Fields Section:
    //----------------------------------------------------------------------
    private nav    : Link[];

    //----------------------------------------------------------------------
    // Constructor Method Section:
    //----------------------------------------------------------------------
    constructor(pNav: Link[])
    {
        this.nav = [];

        if (pNav.length > 0)
        {
            this.nav = pNav;
        }
    }

    //----------------------------------------------------------------------
    // Public Properties Section:
    //----------------------------------------------------------------------
    public get Nav(): Link[]
    {
        return this.nav;
    }
    //----------------------------------------------------------------------
    public set Nav(value: Link[])
    {
        this.nav = value;
    }
}