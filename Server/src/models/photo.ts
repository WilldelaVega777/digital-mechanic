//--------------------------------------------------------------------------------------------------
// Public Class: Photo
//--------------------------------------------------------------------------------------------------
export class Photo
{
    //----------------------------------------------------------------------------------------------
    // Private Fields Section
    //----------------------------------------------------------------------------------------------
    private _id                     : number;
    private _title                  : string = '';
    private _description            : string = '';
    private _picture                : string = '';
    private _src                    : string = '';
    private _srct                   : string = '';
    private _kind                   : string = '';


    //----------------------------------------------------------------------------------------------
    // Constructor Methods Section
    //----------------------------------------------------------------------------------------------
    constructor();
    //----------------------------------------------------------------------------------------------
    constructor(
                    id                  : number,
                    title               : string,
                    description         : string,
                    picture             : string,
                    src                 : string,
                    srct                : string, 
                    kind                : string
    );
    //----------------------------------------------------------------------------------------------
    constructor(
                    id?                  : number,
                    title?               : string,
                    description?         : string,
                    picture?             : string,
                    src?                 : string,
                    srct?                : string,
                    kind?                : string
    )
    {
        if (title)
        {
            this._id                    = id;
            this._title                 = title;
            this._description           = description;
            this._picture               = picture;
            this._src                   = src;
            this._srct                  = srct;
            this._kind                  = kind;
        }
    }

    //----------------------------------------------------------------------------------------------
    // Public Properties Section
    //----------------------------------------------------------------------------------------------
    // Id
    //----------------------------------------------------------------------------------------------
    public get id() : number
    {
        return this._id;
    }
    //----------------------------------------------------------------------------------------------
    public set id(value: number)
    {
        this._id = value;
    }

    //----------------------------------------------------------------------------------------------
    // Title
    //----------------------------------------------------------------------------------------------
    public get title() : string
    {
        return this._title;
    }
    //----------------------------------------------------------------------------------------------
    public set title(value: string)
    {
        this._title = value;
    }

    //----------------------------------------------------------------------------------------------
    // Description
    //----------------------------------------------------------------------------------------------
    public get description() : string
    {
        return this._description;
    }
    //----------------------------------------------------------------------------------------------
    public set description(value: string)
    {
        this._description = value;
    }

    //----------------------------------------------------------------------------------------------
    // Picture
    //----------------------------------------------------------------------------------------------
    public get picture() : string
    {
        return this._picture;
    }
    //----------------------------------------------------------------------------------------------
    public set picture(value: string)
    {
        this._picture = value;
    }

    //----------------------------------------------------------------------------------------------
    // Src
    //----------------------------------------------------------------------------------------------
    public get src() : string
    {
        return this._src;
    }
    //----------------------------------------------------------------------------------------------
    public set src(value: string)
    {
        this._src = value;
    }

    //----------------------------------------------------------------------------------------------
    // Srct
    //----------------------------------------------------------------------------------------------
    public get srct() : string
    {
        return this._srct;
    }
    //----------------------------------------------------------------------------------------------
    public set srct(value: string)
    {
        this._srct = value;
    }

    //----------------------------------------------------------------------------------------------
    // Kind
    //----------------------------------------------------------------------------------------------
    public get kind() : string
    {
        return this._kind;
    }
    //----------------------------------------------------------------------------------------------
    public set kind(value: string)
    {
        this._kind = value;
    }
}